echo "=== Fixing Simpex manufacturer names ===\n";
$fixed = 0;
foreach ( $manufacturer_fixes as $sku => $manufacturer ) {
    $args = array(
        'post_type'  => 'product',
        'meta_key'   => '_sku',
        'meta_value' => $sku,
        'fields'     => 'ids',
        'posts_per_page' => 1,
    );
    $ids = get_posts( $args );
    if ( empty( $ids ) ) continue;
    
    $product_id = $ids[0];
    
    // Update ACF/meta field
    update_post_meta( $product_id, 'manufacturer_brand', $manufacturer );
    
    // Update WooCommerce product attribute 'hersteller'
    $product = wc_get_product( $product_id );
    if ( $product ) {
        // Ensure the term exists
        $term = term_exists( $manufacturer, 'pa_hersteller' );
        if ( ! $term ) {
            $term = wp_insert_term( $manufacturer, 'pa_hersteller' );
        }
        if ( ! is_wp_error( $term ) ) {
            wp_set_object_terms( $product_id, $manufacturer, 'pa_hersteller' );
        }
        
        // Update product attributes to include hersteller if not already there
        $attrs = $product->get_attributes();
        if ( ! isset( $attrs['pa_hersteller'] ) ) {
            $attr = new WC_Product_Attribute();
            $attr->set_name( 'pa_hersteller' );
            $attr->set_options( array( $manufacturer ) );
            $attr->set_visible( true );
            $attr->set_variation( false );
            $attrs['pa_hersteller'] = $attr;
            $product->set_attributes( $attrs );
            $product->save();
        }
    }
    
    $fixed++;
    echo "  Fixed $sku -> $manufacturer\n";
}
echo "Fixed $fixed manufacturer names.\n\n";

